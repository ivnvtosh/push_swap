int	ft_isdigit(int c);

int	ft_atoi(const char *s)
{
	int	n;
	int	sign;

	n = 0;
	sign = 1;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == 43 || *s == 45)
		if (*s++ == 45)
			sign = -1;
	while (ft_isdigit(*s))
		n = n * 10 + *s++ - 48;
	return (n * sign);
}
